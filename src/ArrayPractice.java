public class ArrayPractice {
    public static void main(String[] args) {
        String[] names = {
                "David",
                "Qian",
                "Emina",
                "Jamal",
                "Ashenafi",
                "Mushe"
        };

        for (int i = 0; i < names.length; i++) {
            System.out.println("Hi " + names[i] + ".");
        }
    }
}
